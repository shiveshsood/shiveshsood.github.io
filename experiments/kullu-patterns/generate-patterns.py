"""
Generate 6 Kullu Patti SVG patterns based on traditional motifs.
Each pattern is a tileable vertical strip using a pixel grid.
"""

CELL = 4  # px per cell
COLS = 24  # width in cells

# Traditional Kullu color palettes
PALETTE_A = {
    'R': '#C62828',  # Red
    'G': '#2E7D32',  # Green
    'Y': '#DAA520',  # Gold/Yellow
    'W': '#F5F3ED',  # White (cream)
    'K': '#1A1918',  # Black
    'B': '#1565C0',  # Blue
    'O': '#E65100',  # Orange
    'M': '#6A1B9A',  # Maroon/Purple
    'T': '#00695C',  # Teal
    'P': '#AD1457',  # Pink/Magenta
}

def grid_to_svg(grid_str, palette, filename, cols=COLS):
    rows = [line for line in grid_str.strip().split('\n') if line.strip()]
    height = len(rows)
    width = cols

    svg_w = width * CELL
    svg_h = height * CELL

    rects = []
    for y, row in enumerate(rows):
        for x, ch in enumerate(row[:cols]):
            if ch == ' ' or ch == '.':
                continue
            color = palette.get(ch, '#888888')
            rects.append(
                f'<rect x="{x*CELL}" y="{y*CELL}" width="{CELL}" height="{CELL}" fill="{color}"/>'
            )

    svg = f'''<svg xmlns="http://www.w3.org/2000/svg" width="{svg_w}" height="{svg_h}" shape-rendering="crispEdges">
<rect width="{svg_w}" height="{svg_h}" fill="{palette.get('_bg', '#C62828')}"/>
{''.join(rects)}
</svg>'''

    with open(filename, 'w') as f:
        f.write(svg)
    print(f"Generated {filename}: {svg_w}x{svg_h}px")


